package system.dev.base;

public class JSONPeople {

	private int id;
	private String name;
	private int age;
	private String email;
	private String employment;
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getEmail() {
		return email;
	}
	public String getEmployment() {
		return employment;
	}
	
}
